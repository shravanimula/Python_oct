#capitalize() only capitalizes the first letter of a string and lowers all the remaining characters

s="this Is Python"
print("original string:",s)
print(s.capitalize())

s1="this is new python code"
print("original string:",s1)
print("after using capitalize:",s1.capitalize())


string = "hello good morning"
string1 = string.capitalize()
