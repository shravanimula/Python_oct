
#Python String istitle() Method is a built-in string function that returns True if all the words in the string are title cased, otherwise returns False.
s1="Hello"
print(s1.istitle())

s2="hello Hi Hello"
print(s2.istitle())

s3="Hello Hi Hello"
print(s3.istitle())

